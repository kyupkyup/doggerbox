package recipe;

public class Recipe {
	int productPrimeNum;
	int ingredientPrimeNum;
	String recipeName;
	double mixRate;

	
	public int getProductPrimeNum() {
		return productPrimeNum;
	}
	public void setProductPrimeNum(int productPrimeNum) {
		this.productPrimeNum = productPrimeNum;
	}
	public int getIngredientPrimeNum() {
		return ingredientPrimeNum;
	}
	public void setIngredientPrimeNum(int ingredientPrimeNum) {
		this.ingredientPrimeNum = ingredientPrimeNum;
	}
	public String getRecipeName() {
		return recipeName;
	}
	public void setRecipeName(String recipeName) {
		this.recipeName = recipeName;
	}
	public double getMixRate() {
		return mixRate;
	}
	public void setMixRate(double mixRate) {
		this.mixRate = mixRate;
	}
	
	
}
