package round;

public class Round {
	String roundDeliveryDate;
	int roundPrimeNum;
	String roundTitle;
	
	public String getRoundDeliveryDate() {
		return roundDeliveryDate;
	}
	public void setRoundDeliveryDate(String roundDeliveryDate) {
		this.roundDeliveryDate = roundDeliveryDate;
	}
	public int getRoundPrimeNum() {
		return roundPrimeNum;
	}
	public void setRoundPrimeNum(int roundPrimeNum) {
		this.roundPrimeNum = roundPrimeNum;
	}
	public String getRoundTitle() {
		return roundTitle;
	}
	public void setRoundTitle(String roundTitle) {
		this.roundTitle = roundTitle;
	}
	
	
}
