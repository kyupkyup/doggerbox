package recipeList;

public class RecipeList {
	int recipePrimeNum;
	int puppyPrimeNum;
	String productName;
	int recipeAvailable;
	int productPrimeNum;
	String recipeDate;
	
	public int getRecipePrimeNum() {
		return recipePrimeNum;
	}
	public void setRecipePrimeNum(int recipePrimeNum) {
		this.recipePrimeNum = recipePrimeNum;
	}
	public int getPuppyPrimeNum() {
		return puppyPrimeNum;
	}
	public void setPuppyPrimeNum(int puppyPrimeNum) {
		this.puppyPrimeNum = puppyPrimeNum;
	}

	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public int getRecipeAvailable() {
		return recipeAvailable;
	}
	public void setRecipeAvailable(int recipeAvailable) {
		this.recipeAvailable = recipeAvailable;
	}
	public int getProductPrimeNum() {
		return productPrimeNum;
	}
	public void setProductPrimeNum(int productPrimeNum) {
		this.productPrimeNum = productPrimeNum;
	}
	public String getRecipeDate() {
		return recipeDate;
	}
	public void setRecipeDate(String recipeDate) {
		this.recipeDate = recipeDate;
	}


	
	
}
