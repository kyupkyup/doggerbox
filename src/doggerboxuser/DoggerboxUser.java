package doggerboxuser;

public class DoggerboxUser {
	int userPrimeNum;
	String userName;
	int userBasicAddressNum;
	String userAddress;
	String userPhoneNum;
	String userIntroRoute;
	int userRoutine;
	int userTerm;
	public int getUserPrimeNum() {
		return userPrimeNum;
	}
	public void setUserPrimeNum(int userPrimeNum) {
		this.userPrimeNum = userPrimeNum;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public int getUserBasicAddressNum() {
		return userBasicAddressNum;
	}
	public void setUserBasicAddressNum(int userBasicAddressNum) {
		this.userBasicAddressNum = userBasicAddressNum;
	}
	public String getUserAddress() {
		return userAddress;
	}
	public void setUserAddress(String userAddress) {
		this.userAddress = userAddress;
	}
	public String getUserPhoneNum() {
		return userPhoneNum;
	}
	public void setUserPhoneNum(String userPhoneNum) {
		this.userPhoneNum = userPhoneNum;
	}
	public String getUserIntroRoute() {
		return userIntroRoute;
	}
	public void setUserIntroRoute(String userIntroRoute) {
		this.userIntroRoute = userIntroRoute;
	}
	public int getUserRoutine() {
		return userRoutine;
	}
	public void setUserRoutine(int userRoutine) {
		this.userRoutine = userRoutine;
	}
	public int getUserTerm() {
		return userTerm;
	}
	public void setUserTerm(int userTerm) {
		this.userTerm = userTerm;
	}
	
	
}
