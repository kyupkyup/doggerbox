package ingredient;

public class Ingredient {
	int ingredientPrimeNum;
	String rawIngredient;
	double lossRate;
	double ingredientPrice;
	double sum;
	
	public int getIngredientPrimeNum() {
		return ingredientPrimeNum;
	}
	public void setIngredientPrimeNum(int ingredientPrimeNum) {
		this.ingredientPrimeNum = ingredientPrimeNum;
	}
	public String getRawIngredient() {
		return rawIngredient;
	}
	public void setRawIngredient(String rawIngredient) {
		this.rawIngredient = rawIngredient;
	}
	public double getLossRate() {
		return lossRate;
	}
	public void setLossRate(double lossRate) {
		this.lossRate = lossRate;
	}
	public double getIngredientPrice() {
		return ingredientPrice;
	}
	public void setIngredientPrice(double ingredientPrice) {
		this.ingredientPrice = ingredientPrice;
	}
	public double getSum() {
		return sum;
	}
	public void setSum(double sum) {
		this.sum = sum;
	}
	
	
	
}
